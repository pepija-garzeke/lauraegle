#izveidot vārdnīcu ar skaitļa tipa atslēgām    šis ir otrs vārdnīcas pieraksta veids
dict1={
    18: "Tev",
    21: "garšo",
    3: "āboli"
}
print("Vārdnīca ar skaitļa tipa atslēgām")
print(dict1[18])
print(dict1[21])
print(dict1[3])

#izveido vārdnīcu ar non-numeric keys
